export function renderIcon(name) {
  const icon = ICONS[name]
  if (!icon) return ''

  return `<svg class="icon icon-${name}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${icon}</svg>`
}

export function initIcons() {
  document.querySelectorAll('[data-icon]').forEach((el) => {
    const name = el.dataset.icon
    if (!name || el.querySelector('.icon')) return

    const icon = renderIcon(name)
    if (!icon) return

    el.insertAdjacentHTML('afterbegin', icon)
    el.classList.add('has-icon')
  })
}
